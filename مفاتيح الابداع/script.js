// البحث من شريط البحث
document.getElementById('search').addEventListener('input', function(e) {
    const query = e.target.value.toLowerCase();
    const results = expressions.filter(exp => exp.title.toLowerCase().includes(query));
    displayResults(results);
});

// عرض التعبير بناءً على التصنيف
document.querySelectorAll('nav ul li a').forEach(link => {
    link.addEventListener('click', function(e) {
        e.preventDefault(); // منع التنقل الافتراضي
        const category = e.target.getAttribute('data-category');
        const results = expressions.filter(exp => exp.category === category);
        displayResults(results);
    });
});

// دالة لعرض النتائج
function displayResults(results) {
    const container = document.getElementById('expressions');
    container.innerHTML = ''; // مسح النتائج السابقة
    if (results.length === 0) {
        container.innerHTML = '<p>لم يتم العثور على نتائج.</p>';
    } else {
        results.forEach(result => {
            const div = document.createElement('div');
            div.classList.add('expression');
            div.innerHTML = `
                <h3>${result.title}</h3>
                <p>${result.content}</p>
            `;
            container.appendChild(div);
        });
    }
}
